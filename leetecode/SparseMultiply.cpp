unordered_map<int,unordered_map<int,int>> convertToHm(vector<vector<int>> sparse)
{
    unordered_map<int,unordered_map<int,int>> hm;
    // nested loop for sparse
    for(int i=0;i<sparse.size();i++)
    {
        unordered_map<int,int> row;
        for(int j=0;j<sparse[i].size();j++)
        {
            if(sparse[i][j]!=0)
            {
                row[j]=sparse[i][j];
            }
        }
        if(row.size()!=0)
        {
            hm[i]=row;
        }
    }
    return hm;
}

vector<vector<int>> sparseMultiply(vector<vector<int>> &a,vector<vector<int>> &b)
{
    auto ahm = convertToHm(a);
    auto bhm = convertToHm(b);
    int am = a.size();
    vector<vector<int>> res(am,vector<int>(b[0].size(),0));
    int col,row;
    for(auto it:ahm){
        row = it.first;
        for(auto itr:it.second)
        {
            if(bhm.count(itr.first))
            {
                for(auto itc:bhm[itr.first])
                {
                    col = itc.first;
                    res[row][col] = itc.second*itr.second;
                }
            }
            
        }
    }
    return res;

}

vector<vector<int>> matrixMultiply(vector<vector<int>> &a,vector<vector<int>> &b)
{
    int am = a.size();
    int an = a[0].size();
    int bm = b.size();
    int bn = b[0].size();
    vector<vector<int>> res(am,vector<int>(bn,0));
    for(int i=0;i<am;i++)
    {
        for(int j=0;j<bn;j++)
        {
            for(int k=0;k<an;k++)
            {
                res[i][j]+=a[i][k]*b[k][j];
            }
        }
    }
    return res;
};
bool compareTwoMatrix(vector<vector<int>> &a,vector<vector<int>> &b)
{
    int am = a.size();
    int an = a[0].size();
    int bm = b.size();
    int bn = b[0].size();
    if(am!=bm||an!=bn)
    {
        return false;
    }
    for(int i=0;i<am;i++)
    {
        for(int j=0;j<bn;j++)
        {
            if(a[i][j]!=b[i][j])
            {
                return false;
            }
        }
    }
    return true;
}
