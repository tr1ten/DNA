long double v;
long double rec(long double a,long double b,long double c,ll races){
    long double res = c*races*1.0;
    // debug(a,b,c,races);
    if(a>1e-6){
         if(b>1e-6) res += a*rec(a<v ? 0 : a-v,b+min(a,v)*1.0/2*1.0,c+min(a,v)*1.0/2*1.0,races+1);
         else res += a*rec(a<v ? 0 : a-v,b,c+min(a,v),races+1);
         }
    if(b>1e-6){
        if(a>1e-6) res += b*rec(a+min(b,v)*1.0/2*1.0,b<v ? 0 : b-v,c+min(b,v)*1.0/2*1.0,races+1);
        else  res += b*rec(a,b<v ? 0 : b-v,c+min(b,v),races+1);
        }
    return res;
}
